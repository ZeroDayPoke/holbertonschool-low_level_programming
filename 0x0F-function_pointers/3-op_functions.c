#include "3-calc.h"

/**
 * op_add - fun to add nums
 * @a: num1
 * @b: num2
 * Return: int of completed op
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - fun to subtract nums
 * @a: num1
 * @b: num2
 * Return: int of completed op
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - fun to multiply nums
 * @a: num1
 * @b: num2
 * Return: int of completed op
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fun to divide nums
 * @a: num1
 * @b: num2
 * Return: int of completed op
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fun to mod nums
 * @a: num1
 * @b: num2
 * Return: int of completed op
 */
int op_mod(int a, int b)
{
	return (a % b);
}
